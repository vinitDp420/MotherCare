class PatientEmergencyContactWriteSerializer(serializers.ModelSerializer):
    """Write serializer: add/link an emergency contact to a patient."""

    # Allow creating a new contact inline or referencing existing by ID
    contact_id = serializers.UUIDField(
        required=False,
        help_text="UUID of an existing EmergencyContact to link.",
    )
    name = serializers.CharField(
        required=False, write_only=True,
        help_text="Name for a new EmergencyContact (used when contact_id is not provided).",
    )
    phone = serializers.CharField(
        required=False, write_only=True,
        help_text="Phone for a new EmergencyContact.",
    )
    alt_phone = serializers.CharField(
        required=False, write_only=True, allow_blank=True,
    )
    email = serializers.EmailField(
        required=False, write_only=True, allow_blank=True,
    )

    class Meta:
        model = PatientEmergencyContact
        fields = [
            "contact_id",
            "name",
            "phone",
            "alt_phone",
            "email",
            "relationship_type",
            "priority",
        ]

    def validate(self, attrs: dict) -> dict:
        """Must provide either contact_id (existing) or name+phone (new)."""
        contact_id = attrs.get("contact_id")
        name = attrs.get("name")
        phone = attrs.get("phone")

        if not contact_id and not (name and phone):
            raise serializers.ValidationError(
                "Provide either 'contact_id' (existing contact) or "
                "'name' and 'phone' (new contact)."
            )
        return attrs


