class DoctorWriteSerializer(serializers.ModelSerializer):
    """Write serializer for creating/updating a doctor credential."""

    class Meta:
        model = Doctor
        fields = [
            "staff",
            "specialisation",
            "registration_no",
            "available_from",
            "available_to",
        ]

    def validate(self, attrs: dict) -> dict:
        """available_from must be before available_to if both are provided."""
        available_from = attrs.get("available_from")
        available_to = attrs.get("available_to")
        if available_from and available_to and available_from >= available_to:
            raise serializers.ValidationError(
                {"available_to": "available_to must be after available_from."}
            )
        return attrs

    def validate_staff(self, staff: Staff) -> Staff:
        """A Doctor profile cannot be created for the same staff member twice."""
        qs = Doctor.objects.filter(staff=staff)
        if self.instance:
            qs = qs.exclude(pk=self.instance.pk)
        if qs.exists():
            raise serializers.ValidationError(
                "A Doctor profile already exists for this staff member."
            )
        return staff
